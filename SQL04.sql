-- 12.
CREATE VIEW VW_학과별학생수
AS (SELECT DEPARTMENT_NAME, COUNT(DEPARTMENT_NAME) AS "STUDENT_COUNT"
      FROM TB_DEPARTMENT
      JOIN TB_STUDENT USING(DEPARTMENT_NO)
     GROUP BY DEPARTMENT_NAME);

