-- 15. 포기
SELECT CLASS_NO, CLASS_NAME, COUNT()
FROM (SELECT CLASS_NO, CLASS_NAME, RANK() OVER(ORDER BY COUNT(STUDENT_NAME) DESC) AS "누적수강생수(명)"
        FROM TB_STUDENT
        JOIN TB_GRADE USING (STUDENT_NO)
        JOIN TB_CLASS USING (CLASS_NO)
        WHERE SUBSTR(TERM_NO, 1, 4) >= '2006' 
        GROUP BY CLASS_NO, CLASS_NAME)
WHERE "누적수강생수(명)" <= 3;